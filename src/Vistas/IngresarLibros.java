    private void btnCargaMasivaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCargaMasivaActionPerformed
        try {
            // TODO add your handling code here:

            JSONParser parser = new JSONParser();
            JFileChooser fc = new JFileChooser();
            FileNameExtensionFilter filtro = new FileNameExtensionFilter("*.json", "json");
            fc.setFileFilter(filtro);
            int seleccion = fc.showOpenDialog(this);

            if (seleccion == JFileChooser.APPROVE_OPTION) {

                File archivo = fc.getSelectedFile();
                path = archivo.toString();

                System.out.println(path);

                String[] parts;
                parts = path.trim().split("\\\\");
                if (parts.length == 9) {
                    path = "/" + parts[7] + "/" + parts[8];
                    //   foto = parts[8];
                }

                System.out.println(path);
            }

            Object objeto = parser.parse(new FileReader(path));
            JSONObject jsonObject = (JSONObject) objeto;
            JSONArray arreglo = (JSONArray) jsonObject.get("libros");

            for (int i = 0; i < arreglo.size(); i++) {
                JSONObject jsonObject2 = (JSONObject) arreglo.get(i);
                int isbn = Integer.parseInt(jsonObject2.get("ISBN").toString());
                String titulo = jsonObject2.get("Titulo").toString();
                String autor = jsonObject2.get("Autor").toString();
                String categoria = jsonObject2.get("Categoria").toString();
                String edicion = jsonObject2.get("Edicion").toString();
                String año = jsonObject2.get("Año").toString();
                String idioma = jsonObject2.get("Idioma").toString();
                int carnet = cu.getCarnet();

                Categoria c = cg.getCategorias().buscar(categoria);
                if (c == null) {
                    ArbolB nuevo = new ArbolB();
                    cg.getCategorias().crearCategoria(categoria, nuevo);
                    cg.getCategorias().buscar(categoria).getLibros().crearLibro(isbn, titulo, autor, año, edicion, categoria, idioma, carnet);
                } else {
                    c.getLibros().crearLibro(isbn, titulo, autor, año, edicion, categoria, idioma, carnet);
                }
            }
        } catch (FileNotFoundException ex) {
            Logger.getLogger(IngresarLibros.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(IngresarLibros.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ParseException ex) {
            Logger.getLogger(IngresarLibros.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_btnCargaMasivaActionPerformed

