    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
        // TODO add your handling code here:
        Categoria c = cg.getCategorias().buscar(txtCategoria.getText());

        if (c != null) {

            c.getLibros().crearLibro(
                    Integer.parseInt(txtIsbn.getText()),
                    txtTitulo.getText(),
                    txtAutor.getText(),
                    txtAño.getText(),
                    txtEdicion.getText(),
                    txtCategoria.getText(),
                    txtIdioma.getText(),
                    cu.getCarnet());
        } else {
            ArbolB nuevo = new ArbolB();
            cg.getCategorias().crearCategoria(txtCategoria.getText(), nuevo);
            cg.getCategorias().buscar(txtCategoria.getText()).getLibros().crearLibro(
                    Integer.parseInt(txtIsbn.getText()),
                    txtTitulo.getText(),
                    txtAutor.getText(),
                    txtAño.getText(),
                    txtEdicion.getText(),
                    txtCategoria.getText(),
                    txtIdioma.getText(),
                    cu.getCarnet());
        }


    }//GEN-LAST:event_btnAceptarActionPerformed

