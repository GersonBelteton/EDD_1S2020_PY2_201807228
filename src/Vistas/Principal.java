    public void actualizarTabla() {
        modeloTabla.setRowCount(0);

        if (lista.getSelectedValue().equals("todo")) {
            llenarTablaAVL(cg.getCategorias().getRaiz());
        } else {
            Categoria c = cg.getCategorias().buscar(lista.getSelectedValue());
            if (!c.getLibros().isEmpty()) {
                llenarTabla(c.getLibros().getRaiz());
            }
        }

    }

    public void eliminarLibroISBN(Categoria aux, int isbn) {
        if (aux != null) {
            aux.getLibros().eliminar2(isbn);
            eliminarLibroISBN(aux.getIzquierda(), isbn);
            eliminarLibroISBN(aux.getDerecha(), isbn);
        }
    }

    public void eliminarLibroNombre(Categoria aux, String nombre) {
        if (aux != null) {
            aux.getLibros().eliminarPorNombre(nombre);
            eliminarLibroNombre(aux.getIzquierda(), nombre);
            eliminarLibroNombre(aux.getDerecha(), nombre);
        }
    }

    public void llenarTablaAVL(Categoria aux) {

        if (aux != null) {
            llenarTabla(aux.getLibros().getRaiz());
            llenarTablaAVL(aux.getIzquierda());
            llenarTablaAVL(aux.getDerecha());

        }
    }

    public void llenarTabla(NodoLibro nl) {

        Object[] fila;
        if (nl != null) {
            System.out.println();
            for (int i = 0; i < nl.getLibros().length; i++) {
                if (nl.getLibros()[i] != null) {
                    System.out.println(nl.getLibros()[i].getISBN());

                    fila = new Object[8];
                    fila[0] = nl.getLibros()[i].getISBN();
                    fila[1] = nl.getLibros()[i].getTitulo();
                    fila[2] = nl.getLibros()[i].getCategoria();
                    fila[3] = nl.getLibros()[i].getAutor();
                    fila[4] = nl.getLibros()[i].getEdicion();
                    fila[5] = nl.getLibros()[i].getAño();
                    fila[6] = nl.getLibros()[i].getIdioma();
                    fila[7] = nl.getLibros()[i].getCarnet();

                    modeloTabla.addRow(fila);

                    llenarTabla(nl.getLibros()[i].getIzquierda());

                } else {
                    llenarTabla(nl.getLibros()[i - 1].getDerecha());
                    break;
                }

            }

            // System.out.println(nl.getLibros()[nl.getLibros().length-1].getISBN() + " " + nl.getLibros()[nl.getLibros().length-1].getTitulo());
        }
    }

    public void actualizarLista() {
        modeloLista.clear();
        modeloLista.addElement("todo");
        llenarLista(cg.getCategorias().getRaiz());
    }

    public void llenarLista(Categoria aux) {

        if (aux != null) {
            modeloLista.addElement(aux.getNombre());
            llenarLista(aux.getIzquierda());
            llenarLista(aux.getDerecha());

        }
    }

