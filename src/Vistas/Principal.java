    private void btnEliminarLibroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarLibroActionPerformed
        // TODO add your handling code here:

        if (txtEliminar.getText().equals("") && txtEliminarNombre.getText().equals("")) {
            Categoria c = cg.getCategorias().buscar(modeloTabla.getValueAt(tabla.getSelectedRow(), 2).toString());

            c.getLibros().eliminar2(Integer.parseInt(modeloTabla.getValueAt(tabla.getSelectedRow(), 0).toString()));
            actualizarTabla();
        } else if (!txtEliminar.getText().equals("")) {
            eliminarLibroISBN(cg.getCategorias().getRaiz(), Integer.parseInt(txtEliminar.getText()));
            actualizarTabla();
        } else if(txtEliminar.getText().equals("")&&!txtEliminarNombre.getText().equals("")){
              eliminarLibroNombre(cg.getCategorias().getRaiz(), txtEliminarNombre.getText());
            actualizarTabla();
        }


    }//GEN-LAST:event_btnEliminarLibroActionPerformed

