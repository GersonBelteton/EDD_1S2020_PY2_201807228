    private void btnCargaMasivaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCargaMasivaActionPerformed
        // TODO add your handling code here:

        try {
            // TODO add your handling code here:

            JSONParser parser = new JSONParser();
            JFileChooser fc = new JFileChooser();
            FileNameExtensionFilter filtro = new FileNameExtensionFilter("*.json", "json");
            fc.setFileFilter(filtro);
            int seleccion = fc.showOpenDialog(this);

            if (seleccion == JFileChooser.APPROVE_OPTION) {

                File archivo = fc.getSelectedFile();
                path = archivo.toString();

                System.out.println(path);

                String[] parts;
                parts = path.trim().split("\\\\");
                if (parts.length == 9) {
                    path = "/" + parts[7] + "/" + parts[8];
                    //   foto = parts[8];
                }

                System.out.println(path);
            }

            Object objeto = parser.parse(new FileReader(path));
            JSONObject jsonObject = (JSONObject) objeto;
            JSONArray arreglo = (JSONArray) jsonObject.get("Usuarios");

            for (int i = 0; i < arreglo.size(); i++) {
                
                JSONObject jsonObject2 = (JSONObject) arreglo.get(i);
                int carnet = Integer.parseInt(jsonObject2.get("Carnet").toString());
                String nombre = jsonObject2.get("Nombre").toString();
                String apellido = jsonObject2.get("Apellido").toString();
                String carrera = jsonObject2.get("Carrera").toString();
                String password = DigestUtils.md5Hex(jsonObject2.get("Password").toString());
   
            
                cu.getUsuarios().insertar(carnet, nombre, apellido, carrera, password);
                
          
            }
            
            cu.getUsuarios().mostrarTabla();
        } catch (FileNotFoundException ex) {
            Logger.getLogger(IngresarLibros.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(IngresarLibros.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ParseException ex) {
            Logger.getLogger(IngresarLibros.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_btnCargaMasivaActionPerformed

